    private void jbtnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnupdateActionPerformed
        // TODO add your handling code here:
        // Update query
        String username = jtxtusername.getText();
        String qstn = jcmbqstn.getSelectedItem().toString();
        String ans = jtxtans.getText();
        String npass = jtxtnpass.getText();

        try {

            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/healthcenter", "root", "");
            pst = con.prepareStatement("select * from admin where username=? and securityqstn=? AND answer=?");

            pst.setString(1, username);
            pst.setString(2, qstn);
            pst.setString(3, ans);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {

                pst = con.prepareStatement("UPDATE admin SET password=?");
                pst.setString(1, npass);
                pst.executeUpdate();

                JOptionPane.showMessageDialog(null, "Password Reset");
                jtxtusername.setText(null);
                jcmbqstn.setSelectedItem("----");
                jtxtans.setText(null);
                jtxtnpass.setText(null);
            } else if (username != ("username")) {
                JOptionPane.showMessageDialog(null, "User doesn't exist");
                jtxtusername.setText(null);
                jcmbqstn.setSelectedItem("----");
                jtxtans.setText(null);
                jtxtnpass.setText(null);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jbtnupdateActionPerformed

